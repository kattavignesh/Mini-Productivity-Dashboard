/**
 * MOCK DATA
 * ─────────────────────────────────────────────────────────────────────────────
 * In production this data is fetched from Supabase tables:
 *   • profiles         – id, full_name, role, department, avatar_url
 *   • activity_logs    – id, user_id, date, task, hours, status, notes
 *
 * The application applies Supabase Row Level Security (RLS) so that:
 *   • Employees  → can only SELECT rows where user_id = auth.uid()
 *   • Managers   → can SELECT all rows
 *
 * ─────────────────────────────────────────────────────────────────────────────
 * AI INTEGRATION NOTE
 * ─────────────────────────────────────────────────────────────────────────────
 * To generate a weekly summary via an LLM (e.g. Google Gemini or OpenAI GPT-4):
 *
 * 1. A Supabase Edge Function (`generate-weekly-summary`) is triggered every
 *    Monday via a pg_cron job.
 * 2. The function queries all activity_logs for the past 7 days and serialises
 *    them to a concise JSON payload.
 * 3. The payload is sent to the LLM API with a structured prompt:
 *
 *      "You are an HR analytics assistant. Given the following employee activity
 *       logs for the week of {date_range}, generate a concise executive summary
 *       highlighting: (1) top performers, (2) tasks completed vs tasks pending,
 *       (3) departments with the highest workload. Return the result in Markdown."
 *
 * 4. The LLM response is stored back into a `weekly_summaries` table.
 * 5. The Manager dashboard fetches and renders the latest summary with a
 *    "Generated by AI" badge, allowing managers to quickly audit the week
 *    without reading raw logs.
 *
 * This approach keeps LLM calls server-side (no API key exposure to the client),
 * is cost-efficient (one call per week per organisation), and integrates cleanly
 * with Supabase's existing auth/RLS model.
 * ─────────────────────────────────────────────────────────────────────────────
 */

