/**
 * Simulates the Supabase RLS query:
 *   SELECT * FROM activity_logs
 *   JOIN profiles ON profiles.id = activity_logs.user_id
 *   WHERE (role = 'manager') OR (user_id = auth.uid())
 */
export function getActivityLogs(userId, role) {
  const logs = MOCK_ACTIVITY_LOGS.map((log) => ({
    ...log,
    profile: MOCK_USERS.find((u) => u.id === log.user_id),
  }));

  if (role === 'manager') return logs;
  return logs.filter((l) => l.user_id === userId);
}

export function getStats(logs) {
  const totalHours = logs.reduce((s, l) => s + l.hours, 0);
  const completed = logs.filter((l) => l.status === 'completed').length;
  const inProgress = logs.filter((l) => l.status === 'in-progress').length;
  const pending = logs.filter((l) => l.status === 'pending').length;
  const uniqueEmployees = new Set(logs.map((l) => l.user_id)).size;

  return { totalHours, completed, inProgress, pending, uniqueEmployees };
}

export function getHoursPerEmployee(logs) {
  const map = {};
  logs.forEach((l) => {
    const name = l.profile?.full_name ?? 'Unknown';
    map[name] = (map[name] ?? 0) + l.hours;
  });
  return Object.entries(map).map(([name, hours]) => ({ name, hours }));
}

export function getHoursPerDay(logs) {
  const map = {};
  logs.forEach((l) => {
    map[l.date] = (map[l.date] ?? 0) + l.hours;
  });
  return Object.entries(map)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([date, hours]) => ({
      date: new Date(date).toLocaleDateString('en-IN', { weekday: 'short', day: 'numeric', month: 'short' }),
      hours,
    }));
}
